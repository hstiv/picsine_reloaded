int		ft_sqrt(int nb)
{
	int sqrt;

	sqrt = 0;
	if (nb > 0)
	{
		while ((sqrt * sqrt) <= nb)
		{
			if ((sqrt * sqrt) == nb && (nb % sqrt) == 0)
				return (sqrt);
			sqrt++;
		}
		return (0);
	}
	else
	{
		return (0);
	}
}
